#an operator is a symbol or notation which is used to represent the mathematical operation

a = 10
b = 12

print(a+b) #addition operator

print(b-a) #subtraction operator

print(a*b) #multiplication operator

print(a/b) #division operator

def new():
    global a #changing the values of the variables
    a = 12
new()

print(a%5) #modulus operator

print(a**2) #exponentiation operator or similar to pow function

print(a//5) #flow division operator (it will give only the integer value)

